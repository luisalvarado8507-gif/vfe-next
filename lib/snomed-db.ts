export function getSnomedVTM(vtm: string) {
  return SNOMED_VTM[vtm.toLowerCase().trim()] || null;
}

export function getSnomedFF(ff: string) {
  if (!ff) return null;
  const key = ff.toLowerCase().split('(')[0].trim();
  return SNOMED_FF[key] || SNOMED_FF[ff.toLowerCase().trim()] || null;
}

export function computeVMP(vtm: string, ff: string, conc: string): string {
  if (!vtm) return '';
  return `${vtm} ${conc} ${ff}`.trim();
}

export function computeVMPP(vmp: string, units: string, upres: string): string {
  if (!vmp) return '';
  return `${vmp}${units ? ' ' + units + ' ' : ' '}${upres || 'presentación'}`.trim();
}
